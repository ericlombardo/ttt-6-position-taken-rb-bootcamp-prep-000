def position_taken?(board, index)
  # checking to see if the spot empty
  if board[index] == (" ")
    false
  if board[index] == ("")
    false
  if board[index] == (nil)
    false
  if board[index] == ("X")
    true
  if board[index] == ("O")
    true
  end
end
